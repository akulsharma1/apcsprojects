package boba;

public class drinks {
    String type;
    int icePercentage;
    int sweetnessPercentage;

    public String getType() {
        return type;
    }
    public void setType(String type) {
        this.type = type;
    }

    public int getIcePercentage() {
        return icePercentage;
    }
    public void setIcePercentage(int icePercentage) {
        this.icePercentage = icePercentage;
    }

    public int getSweetnessPercentage() {
        return sweetnessPercentage;
    }
    public void setSweetnessPercentage(int sweetnessPercentage) {
        this.sweetnessPercentage = sweetnessPercentage;
    }
}
